CREATE TABLE CUENTAS
(
  id INTEGER(4) AUTO_INCREMENT,
  propietario VARCHAR(5),
  tipo VARCHAR(10),
  saldo DOUBLE,
  fecha_creacion DATE,
  numero_cuenta VARCHAR(20),
  sucursal INTEGER(4),
  FOREIGN KEY (sucursal) REFERENCES SUCURSALES(id),
  PRIMARY KEY(id)
);



